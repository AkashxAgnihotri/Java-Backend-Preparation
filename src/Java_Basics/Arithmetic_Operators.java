package Java_Basics;

public class Arithmetic_Operators {
    static int x= 10;
    static int b= 20;
    static int c=33;
    static int d=13%2;
    static int y=100;
    public static void main(String[] args) {
        System.out.println(x+b);    //Add
        System.out.println(b-x);    //Sub
        System.out.println(c/11);   //div
        System.out.println(d);      //Modulus
    // Pre fix and post fix-concepts
        System.out.println(y++);
        System.out.println(y);
        System.out.println(y++);
        System.out.println(y);


    }

}