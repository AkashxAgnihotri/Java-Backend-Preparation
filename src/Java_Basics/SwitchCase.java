package Java_Basics;

public class SwitchCase {
    public static void main(String[] args) {
        int x=1;
        switch (x){
            case 1:
                System.out.println("X s 1");
                break;
            case 2:
                System.out.println("X is 2");
            case 3:
                System.out.println("X s 3");
            default:
                System.out.println("X s 4");
        }

    }
}
